extern State state_example;

// --------------------------------------------------
// Program main entry point
// --------------------------------------------------
float dt;

int main(void) {
  SM_ChangeState(&state_example, NULL);

  while (!WindowShouldClose()) {
    dt = GetFrameTime();
    SM_Update(dt);
    SM_Draw();
  }

  return 0;
}

